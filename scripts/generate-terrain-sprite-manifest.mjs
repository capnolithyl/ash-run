import fs from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { TERRAIN_LIBRARY } from "../src/game/content/terrain.js";

const GENERATED_MANIFEST_PATH = "src/game/phaser/generated/terrainSpritePngIds.js";

export async function generateTerrainSpriteManifest({ root = process.cwd() } = {}) {
  const terrainDirectoryPath = path.resolve(root, "assets/sprites/terrain");
  const entries = await fs.readdir(terrainDirectoryPath, { withFileTypes: true });
  const validTerrainIds = new Set(Object.keys(TERRAIN_LIBRARY));
  const terrainIds = entries
    .filter((entry) => entry.isFile() && /\.png$/i.test(entry.name))
    .map((entry) => entry.name.replace(/\.png$/i, ""))
    .filter((terrainId) => validTerrainIds.has(terrainId))
    .sort((left, right) => left.localeCompare(right));
  const animatedTerrainIds = entries
    .filter((entry) => entry.isDirectory() && validTerrainIds.has(entry.name))
    .map((entry) => entry.name);
  const resolvedAnimatedTerrainIds = [];

  for (const terrainId of animatedTerrainIds) {
    const animationSheetPath = path.join(terrainDirectoryPath, terrainId, `${terrainId}.png`);

    try {
      const stats = await fs.stat(animationSheetPath);

      if (stats.isFile()) {
        resolvedAnimatedTerrainIds.push(terrainId);
      }
    } catch {
      // Ignore terrain folders that do not yet contain a matching animation sheet.
    }
  }

  resolvedAnimatedTerrainIds.sort((left, right) => left.localeCompare(right));

  const output = [
    "// This file is generated by scripts/generate-terrain-sprite-manifest.mjs.",
    "// Do not edit by hand.",
    "",
    `export const GENERATED_TERRAIN_SPRITE_PNG_IDS = ${JSON.stringify(terrainIds, null, 2)};`,
    `export const GENERATED_TERRAIN_ANIMATED_IDS = ${JSON.stringify(resolvedAnimatedTerrainIds, null, 2)};`,
    ""
  ].join("\n");
  const manifestPath = path.resolve(root, GENERATED_MANIFEST_PATH);

  await fs.mkdir(path.dirname(manifestPath), { recursive: true });
  await fs.writeFile(manifestPath, output, "utf8");

  return {
    terrainIds,
    animatedTerrainIds: resolvedAnimatedTerrainIds
  };
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await generateTerrainSpriteManifest();
}
