import fs from "node:fs/promises";
import path from "node:path";
import { pathToFileURL } from "node:url";
import { BUILDING_KEYS } from "../src/game/core/constants.js";

const GENERATED_MANIFEST_PATH = "src/game/phaser/generated/buildingSpritePngOwners.js";
const BUILDING_OWNER_VARIANTS = ["player", "enemy", "neutral"];

export async function generateBuildingSpriteManifest({ root = process.cwd() } = {}) {
  const manifest = {};

  for (const buildingTypeId of Object.values(BUILDING_KEYS)) {
    for (const owner of BUILDING_OWNER_VARIANTS) {
      const pngPath = path.resolve(
        root,
        "assets/sprites/buildings",
        owner,
        `${buildingTypeId}.png`,
      );

      try {
        await fs.access(pngPath);
        manifest[buildingTypeId] ??= [];
        manifest[buildingTypeId].push(owner);
      } catch {
        // SVG fallback is handled in the runtime asset manifest.
      }
    }
  }

  const normalizedManifest = Object.fromEntries(
    Object.entries(manifest).map(([buildingTypeId, owners]) => [
      buildingTypeId,
      owners.sort((left, right) => left.localeCompare(right)),
    ]),
  );

  const output = [
    "// This file is generated by scripts/generate-building-sprite-manifest.mjs.",
    "// Do not edit by hand.",
    "",
    `export const GENERATED_BUILDING_SPRITE_PNG_OWNERS = ${JSON.stringify(normalizedManifest, null, 2)};`,
    "",
  ].join("\n");
  const manifestPath = path.resolve(root, GENERATED_MANIFEST_PATH);

  await fs.mkdir(path.dirname(manifestPath), { recursive: true });
  await fs.writeFile(manifestPath, output, "utf8");

  return normalizedManifest;
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  await generateBuildingSpriteManifest();
}
